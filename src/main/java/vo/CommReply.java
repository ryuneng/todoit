package vo;

import java.util.Date;

public class CommReply {
	
	private int no;
	private String content;
	private String deleted;
	private Date createdDate;
	private Date updatedDate;
	private int dangerCnt;
	private Community community;
	private User user;
	private int parentsReply;
	
	public CommReply() {};
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getDeleted() {
		return deleted;
	}
	public void setDeleted(String deleted) {
		this.deleted = deleted;
	}
	public Date getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(Date createdDate) {
		this.createdDate = createdDate;
	}
	public Date getUpdatedDate() {
		return updatedDate;
	}
	public void setUpdatedDate(Date updatedDate) {
		this.updatedDate = updatedDate;
	}
	public int getDangerCnt() {
		return dangerCnt;
	}
	public void setDangerCnt(int dangerCnt) {
		this.dangerCnt = dangerCnt;
	}
	public Community getCommunity() {
		return community;
	}
	public void setCommunity(Community community) {
		this.community = community;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public int getParentsReply() {
		return parentsReply;
	}
	public void setParentsReply(int parentsReply) {
		this.parentsReply = parentsReply;
	}
	@Override
	public String toString() {
		return "CommReply [no=" + no + ", content=" + content + ", deleted=" + deleted + ", createdDate=" + createdDate
				+ ", updatedDate=" + updatedDate + ", dangerCnt=" + dangerCnt + ", community=" + community + ", user="
				+ user + ", parentsReply=" + parentsReply + "]";
	}

}
